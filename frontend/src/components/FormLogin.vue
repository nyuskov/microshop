<script setup lang="ts">
import { Form, type FormSubmitEvent } from '@primevue/forms';
import { FormField } from '@primevue/forms';
import Button from 'primevue/button';
import InputText from 'primevue/inputtext';
import Message from 'primevue/message';
import Password from 'primevue/password';
import { zodResolver } from "@primevue/forms/resolvers/zod";
import { z } from 'zod';
import { useRouter } from 'vue-router';
import {
  backendServer
} from '../stores/auth.ts';
import { ref } from 'vue';

const router = useRouter();
const formSchema = z.object({
  username: z.string(),
  password: z.string(),
});
const resolver = zodResolver(formSchema);
const api_prefix: string = "/api/v1";
const redirectPath = "/auth/register/";
const severity = ref("success");
const result = ref("");

async function loginUser(e: FormSubmitEvent<Record<string, any>>) {
  if (backendServer != undefined) {
    await fetch(
      'https://' + backendServer.address + api_prefix + '/jwt/login/', {
      method: 'POST',
      cache: "reload",
      body: new URLSearchParams(e.values),
      headers: {
        'Content-Type': 'application/x-www-form-urlencoded',
        'X-CSRFToken': backendServer.csrfToken
      },
      credentials: 'include',
    }).then(async function (response) {
      result.value = (await response).statusText;
      if (response.status == 200) {
        router.push('/');
      } else {
        severity.value = "error";
      }
    }).catch((error) => {
      result.value = error;
      severity.value = "error";
    });
  }
}
async function onFormSubmit(e: FormSubmitEvent<Record<string, any>>) {
  console.log(e);
  if (Object.keys(e.errors).length) {
    return;
  }
  await loginUser(e);
}
</script>

<template>
  <div class="cnt-login">
    <Form @submit="onFormSubmit" :resolver class="frm-login flex flex-col gap-4 w-full sm:w-80">
      <h3>Вход</h3>
      <FormField v-slot="$field" name="username" initialValue="" class="flex txt-login flex-col gap-1">
        <InputText type="text" class="txt-login" placeholder="Имя пользователя" />
        <Message v-if="$field?.invalid" severity="error" size="small" variant="simple">{{ $field.error?.message }}
        </Message>
      </FormField>
      <FormField v-slot="$field" name="password" initialValue="" class="flex txt-login flex-col gap-1">
        <Password type="text" class="txt-login" placeholder="Пароль" :feedback="false" toggleMask fluid />
        <Message v-if="$field?.invalid" severity="error" size="small" variant="simple">{{ $field.error?.message }}
        </Message>
      </FormField>
      <Message size="small" :severity variant="simple">{{ result }}</Message>
      <Button type="submit" class="btn-login" label="Войти" />
      <Button @click="router.push(redirectPath)" class="btn-login" label="Зарегистрироваться" severity="secondary"
        variant="text" />
    </Form>
  </div>
</template>

